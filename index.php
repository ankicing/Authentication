The most common method used for  authentication is textual password. The vulnerabilities of this method like eves dropping, dictionary attack, social engineering and shoulder surfing are well  known.  Random  and  lengthy  passwords  can  make  the  system  secure.  But  the  main problem is the difficulty of remembering those passwords. Studies have shown that users tend to  pick  short  passwords  or  passwords  that  are  easy  to  remember.  Unfortunately,  these passwords can be easily guessed or cracked. The alternative techniques are graphical passwords and  biometrics.  But  these  two  techniques  have  their  own  disadvantages.  Biometrics,  such  as finger prints, iris scan or facial recognition have been introduced  but not  yet widely adopted. The  major  drawback  of  this  approach  is  that  such  systems  can  be  expensive  and  the identification  process  can  be  slow.  There  are  many  graphical  password  schemes  that  are proposed in the last decade. But most of them suffer from shoulder surfing which is becoming quite a big problem. There are graphical passwords schemes that have been proposed which are resistant to shoulder-surfing but they have their own drawbacks like usability issues or taking more  time  for  user  to  login  or  having  tolerance  levels. Personal  Digital  Assistants  are  being used by the people to store their personal and confidential information like passwords and PIN numbers. Authentication should be provided for the usage of these devices.
